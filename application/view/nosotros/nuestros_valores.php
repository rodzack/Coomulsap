                <br/><h2 class="greenTitle">Nuestros Valores</h2><br/>
                <p>Coomulsap trabaja enmarcado en los valores cooperativos:</p><br/>

                <h4 class="greenTitle">Ayuda Mutua</h4><br/>
                <p>Se da la “ayuda mutua” cuando dos o más personas se socorren y cooperan entre sí para lograr  las metas individuales o colectivas propuestas.</p><br/>

                <h4 class="greenTitle">Responsabilidad</h4><br/>
                <p>La “responsabilidad” es la obligación de responder por los propios actos. Es también garantizar el  cumplimiento de los compromisos adquiridos.</p><br/>

                <h4 class="greenTitle">Democracia</h4><br/>
                <p>En el cooperativismo hay “democracia” cuando los(as) asociados(as) mantienen el control de la  cooperativa participando activamente en la toma de decisiones en asambleas generales, en  órganos sociales a través de sus representantes o en otros espacios de poder.</p><br/>

                <h4 class="greenTitle">Igualdad</h4><br/>
                <p>Consiste en ofrecer el mismo trato y condiciones de desarrollo a cada asociado(a) sin discriminación de sexo, etnia, clase social, credo y capacidad intelectual o física.</p><br/>

                <h4 class="greenTitle">Equidad</h4><br/>
                <p>La “equidad” se refiere a la noción de justicia de dar a cada cual lo que se merece o ha ganado según sea su grado de participación o aporte y reconociendo sus condiciones y características especiales. Es decir, tomando en cuenta la diferencia.</p><br/>

                <h4 class="greenTitle">Solidaridad</h4><br/>
                <p>Es la adhesión libre y voluntaria a una causa o empresa, creando una relación humana de mutuo apoyo donde la felicidad particular depende de la felicidad colectiva. Es entonces la comunidad de intereses, sentimientos y propósitos.</p><br/>

                <h4 class="greenTitle">Honestidad</h4><br/>
                <p>Es la honradez, la dignidad y la decencia en la conducta de los(as) asociados(as). La persona honesta es incapaz de robar, estafar o defraudar.</p><br/>

                <h4 class="greenTitle">Transparencia</h4><br/>
                <p>En una cooperativa hay “transparencia” cuando la información es administrada entre asociados(as) y dirigentes de manera clara, veraz y oportuna. En la base de la transparencia está la confianza, la comunicación y la franqueza. Se oponen a ella el encubrimiento y el engaño.</p><br/>

                <h4 class="greenTitle">Responsabilidad Social</h4><br/>
                <p>En el cooperativismo la “responsabilidad social” se vincula al compromiso con el desarrollo de la comunidad. Por ello las cooperativas son agentes activos en la generación de empleo y en la justa distribución de la riqueza.</p><br/>

                <h4 class="greenTitle">Preocupación por los Demás</h4><br/>
                <p>Hasta no lograr el bienestar material y espiritual de las personas que le rodean, el cooperativista auténtico estará en una condición de inquietud o desasosiego, buscando soluciones a los problemas sociales. Eso es “preocupación por los demás”.</p><br/>

